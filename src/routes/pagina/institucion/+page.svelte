         <p>La Institución Educativa "LOLA GONZÁLEZ" debe su nombre a la educadora Lola González
            Mesa que nació y murió en Medellín (1894–1970). Hija de don Carlos González y doña
            Carmen Mesa. Rectora por 16 años (1936-1952) en el Instituto Central Femenino (Actual
            CEFA) y luego Directora de la Escuela de Bellas Artes.
            Consagró toda su vida a la educación. El Ministerio de Educación Nacional le concedió la
            Medalla "Estrella Antioquia". En reconocimiento a sus valores como pedagoga, la Asamblea
            Departamental dio su nombre al Instituto Femenino de Bachillerato Técnico Comercial de
            San Javier.
            <br>
            <br>
            La Institución Educativa Lola González, nace a partir de la Ordenanza Nº 28 de la Asamblea
          Departamental en el año 1962, con el impulso dado por el Presbítero Pedro Gómez, Párroco
          de San Javier, quien originó la idea de colaborar en la formación educativa de las jóvenes del
          Barrio San Javier y los sectores cercanos. A la señora directora Olga Medina de Díaz, con la
        colaboración de la Inspectora Departamental, Albertina Moreno se les encargó la iniciación
        del nuevo instituto; estas dos personas iniciaron matrículas el 28 de enero de 1963 (año que
        se tiene en cuenta para los cumpleaños de la Institución) y el 12 del siguiente mes se dio
        inicio a las clases con un total de 330 alumnas (este año 2013 la Sede Lola González cumple
          50 años de haber iniciado labores). 
        
        
          
        
        <p>
        Se inicia en el teatro del Barrio San Javier y en la calle, más adelante con la colaboración de
        los vecinos, el párroco logra conseguir varios garajes y se instala en la cripta del templo el
        segundo año. A partir de este hecho se empiezan a realizar varias gestiones para la
        consecución de un local adecuado para el establecimiento, hecho que se consolida en
        septiembre de 1978, con la bendición y colocación de la primera piedra para la construcción
        del local definitivo. En el mes de febrero de 1983 
          
    
     
        (cuando cumplió 20 años) se produce el
        traslado de lo que popularmente se llamaba “Lola Garajes” a la sede que actualmente
        ocupa en la Calle 47F Nº 94-63, Barrio Floresta Santa Lucía, Comuna 12, en límites con la
        Comuna 13, atendiendo mayoritariamente a estudiantes de la Comuna 13.
        <br>
        <br>
        Hasta ese año (1983) las chicas que terminaban Grado Noveno (9º), se les daba el certificado de Auxiliares
        en Contabilidad y Secretariado, y para terminar su bachillerato debían pasar al CEFA o a
        otras Instituciones Educativas. La primera promoción de bachilleres lolistas se graduó en
        diciembre de 1984. La Sede Lola González (Bachillerato) fue exclusivamente femenina hasta
        el año 2004. Se comenzó a volver mixta paulatinamente a partir del año 2005, con un solo
        niño en el Grado Sexto (6º), hasta llegar a ser totalmente mixta en el año 2010 (con 690
        estudiantes hombres de un total de 2800 estudiantes que tiene la Institución en sus dos
        sedes, o sea un 24%), cuando se graduó la la primera promoción mixta de bachilleres.
    
    </p>
    <h2>Creación De La Sede Santa Lucía</h2>
    
    
    <p> 
        En 1959, la Escuela Doméstica de Antioquia, que tres años después recibe el nombre de
Escuela Santa Lucía, inicia sus labores con 260 niñas, cuatro profesoras y la Directora
Hermana Cristina María.
<br>
<br>
La planta física para ese entonces era una construcción en forma de “ele”, contaba con 5
aulas de clase y un espacio amplio (el actual Salón Nº 11 de Preescolar) que fue utilizado
como capilla del barrio, mientras se construía el actual templo de la Parroquia de Santa
Lucía.
<br>
<br>
En 1961 fue nombrada en propiedad como directora la Señora Lía Ruíz de Córdoba (madre
de la Senadora Piedad Córdoba). El horario de aquella época era de doble jornada de lunes
a sábado.
<br>
<br>
En 1963 llega a la Escuela como directora la Señora Fanny Villegas Sepúlveda.
<br>
<br>
En 1966 se inauguraron nuevas aulas y el restaurante escolar. En 1968 se da inicio a las dos
jornadas académicas. Para el año 1970 nombran como directora de la escuela a la Señora
Consuelo Barrera, y en septiembre del mismo año a la Señora Ofelia Ramírez (q.e.p.d.).
<br>
<br>
Para el año 1976, nombran como nueva directora a la Señora Gilma Pérez.
<br>
<br>
En 1980, Rubiela Pérez Galvis, quien venía desempeñándose como docente de la Escuela
desde 1969, es nombrada directora, postulada por todas sus compañeras docentes de ese
entonces.
<br>
<br>
En 2009, al cumplir la Sede Santa Lucía sus Bodas de Oro, Rubiela Pérez Galvis cumplió 40
años de eficiente gestión docente y directiva que han convertido esta Sede en una de las
más reconocidas de la ciudad.
    </p>
